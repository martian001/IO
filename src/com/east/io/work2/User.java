public class User implements Serializable {
	private int age;
	private String name;

	public User(int age, String name) {
		this.age = age;
		this.name = name;
	}

	public User() {
	}

	@Override
	public String toString() {
		return "name=" + name + ", age=" + age;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
